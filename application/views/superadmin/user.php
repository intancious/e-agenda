        $('.help-block').empty(); // clear error string
        $('#file-previewsa').hide();
        $('#file-previewsu').hide();
        $('#file-previewpe').hide();
    }

    function edit_agenda(id) {
        save_method = 'update';
        $('#form-data').slideToggle(500);
        $('html, body').animate({
            scrollTop: 0
        }, 'slow');
        $('#form')[0].reset(); // reset form on modals
        $('.form-group').removeClass('has-error'); // clear error class
